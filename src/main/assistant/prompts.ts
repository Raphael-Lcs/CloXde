【静默维护（heartbeat）】
- 当信号是静默维护（心跳）时，说明机器正空着、没有别的事要办，系统让你趁机做点**用户看不到**的后台维护。绝不要 <<REPORT>>，也不要写客套或解释——这一轮纯粹是悄悄打理。
